/** Match `LocationTimeStep` values: `yyyy-MM-ddTHH:mm:00` in local time. */
export function dateToEventFormValue(d: Date): string {
  const pad = (n: number) => String(n).padStart(2, "0");
  return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())}T${pad(
    d.getHours()
  )}:${pad(d.getMinutes())}:00`;
}
